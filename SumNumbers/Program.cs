using System;

class Program
{
    static void Main()
    {
        // Read the input values for A and B
        int A = int.Parse(Console.ReadLine());
        int B = int.Parse(Console.ReadLine());

        // Calculate the sum of A and B
        int X = A + B;

        // Print the result in the required format
        Console.WriteLine($"X = {X}");
    }
}
